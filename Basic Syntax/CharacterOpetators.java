public class CharacterOpetators {
    public static void main(String[] args) {
        char firstChar = 'a';
        char secondChar = 'b';
        // arithmetic operator when used on character it will give result in int
        int ans = firstChar + secondChar;
        System.out.println(ans);
        ans = firstChar * secondChar;
        System.out.println(ans);
    }
}
