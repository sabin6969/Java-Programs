public class PrimitiveDataTypes {
    public static void main(String[] args) {
        // Data types are classified into two types
        // 1.Primitive and Non primitive data types
        // Examples of primitive data types
        // byte, short, int, long, float, double, boolean, char

        // Primitive data types cannot be further decomposed or breakdown

        // byte takes 1 byte of memory
        byte age = 20;

        // short takes 2 bytes of memory
        short salary = 4500;

        // int takes 4 bytes of memory
        int populationOfNepal = 334234234;

        // long takes 8 bytes of memory and we must end long data type with l or
        long populationOfWorld = 3442342342323433L;

        // float takes 4 bytes of memory, must end with f or F, has lower precision
        float height = 5.82f;
        // double takes 8 bytes of memory and has higher precision
        double heightNew = 234234234.234234234;

        // boolean takes 1 byte of memory and supports true and false only
        boolean isLoggedIn = true;
        boolean isMarried = false;

        // char takes 2 bytes of memory
        // 1 is for unicode
        char gender = 'M';
    }
}
