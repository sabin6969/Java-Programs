
public class PositiveNegativeZero {
    public static void main(String[] args) {
        int number = 33;
        if (number > 0) {
            System.out.println("Number is positive");
        } else if (number < 0) {
            System.out.println("Number is Negative");
        } else {
            System.out.println("Number is Zero");
        }
    }
}